package sec02.exam01;

public class 리터럴연습1 {
	public static void main(String[] args) {
		int var1= 0b1011; // 2진수
		int var2= 0206; // 8진수
		int var3= 365; // 10진수
		int var4= 0xb3; // 16진수
		
		// 0b -> 2진수 0-> 8진수 0x->16진수 
		
		System.out.println("var1: " +var1);
		System.out.println("var2: " +var2);
		System.out.println("var3: " +var3);
		System.out.println("var4: " +var4);
	}

}
